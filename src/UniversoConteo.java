public class UniversoConteo {
    private String universo;
    private int conteo;

    public UniversoConteo(String universo, int conteo) {
        this.universo = universo;
        this.conteo = conteo;
    }

    public String getUniverso() {
        return universo;
    }

    public void setConteo(int conteo) {
        this.conteo = conteo;
    }

    public int getConteo() {
        return conteo;
    }
}
